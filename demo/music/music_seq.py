def generate_seq(pitch_list, octave=4):
    """
    Generate note sequence given pitch class input
    :param pitch_list:
    :param octave: default C4
    :return:
    """
    return [i % 12 + 12 * (i // 12) + 12 * (octave + 1) for i in pitch_list]


